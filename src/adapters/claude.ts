import type { Adapter, GeneratedFile, RenderContext } from "./base.js";
import type { MemorySet } from "../core/memory.js";
import { renderFile, buildStandardBody } from "./_render.js";

const NAME = "claude";
const VERSION = "0.1.0";

export const claudeAdapter: Adapter = {
  name: NAME,
  version: VERSION,
  outputPaths: ["CLAUDE.md"],

  async render(memory: MemorySet, ctx: RenderContext): Promise<GeneratedFile[]> {
    const file = renderFile({
      adapterName: NAME,
      adapterVersion: VERSION,
      outputPath: "CLAUDE.md",
      memory,
      ctx,
      buildBody: (sections, truncated) =>
        buildStandardBody(
          sections,
          truncated,
          `# ${ctx.projectName} — Agent Context (Claude)\n\nThis file is auto-generated by agentctx. Edit files in \`agent/\` and run \`agentctx sync\`.`,
        ),
    });
    return [file];
  },
};
